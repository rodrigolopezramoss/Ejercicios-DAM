public class Ejercicio01{
  public static void main(String[] args){
    
    int x = 144;
    int y = 999; 
	
	System.out.println("X vale:" + x);
	System.out.println("Y vale:" + y);
	
	double suma;
	double resta;
	double multiplicación;
	double división;

	suma = x + y;
	resta = x - y;
	multiplicación = (double)x * (double)y;
	división = (double)x / (double)y;

	System.out.println("La suma de ambos vale:"+ suma);
	System.out.println("La resta de ambos vale:"+ resta);
	System.out.println("La división de ambos vale:"+ división);
	System.out.println("La multiplicación de ambos vale:"+ multiplicación);
	
  } 
 }
